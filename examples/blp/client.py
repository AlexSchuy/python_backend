import os
import sys
import argparse
import numpy as np
from tritonclient.utils import *
import tritonclient.http as httpclient

if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument('-v',
                        '--verbose',
                        action="store_true",
                        required=False,
                        default=False,
                        help='Enable verbose output')
    parser.add_argument('-m',
                        '--model-name',
                        type=str,
                        required=True,
                        help='Name of model')
    parser.add_argument('-c',
                        '--classes',
                        type=int,
                        required=False,
                        default=1,
                        help='Number of class results to report. Default is 1.')
    parser.add_argument('-u',
                        '--url',
                        type=str,
                        required=False,
                        default='localhost:8000',
                        help='Inference server URL. Default is localhost:8000.')
    parser.add_argument('image_filename',
                        type=str,
                        nargs='?',
                        default=None,
                        help='Input image')
    FLAGS = parser.parse_args()

    with httpclient.InferenceServerClient(verbose=FLAGS.verbose, url=FLAGS.url) as client:
        image_data = np.fromfile(FLAGS.image_filename, dtype='uint8')
        inputs = [
            httpclient.InferInput("INPUT", image_data.shape,
                                  np_to_triton_dtype(image_data.dtype))
        ]
        inputs[0].set_data_from_numpy(image_data)

        response = client.infer(FLAGS.model_name, inputs)
        result = response.as_numpy("OUTPUT")
        print(result)
